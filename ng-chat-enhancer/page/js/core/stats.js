(function() {

//------------------------------------------------------------
NGCE.Stats = {
	init: init,
	mentioned: mentioned,
	messageSent: messageSent
};
//------------------------------------------------------------



//------------------------------------------------------------
// Private variables
//------------------------------------------------------------

	var o = NGCE.ChromeSync.Stats;

//------------------------------------------------------------



//------------------------------------------------------------
// Private
//------------------------------------------------------------


//------------------------------------------------------------



//------------------------------------------------------------
// Public
//------------------------------------------------------------

function init() {
	o.load();
	NGCE.Helper.Watch.watch('mentioned', NGCE.Stats.mentioned);
};

function mentioned() {
	o.Data.mentioned = (o.Data.mentioned || 0) + 1;
	o.save();
};

function messageSent() {
	o.Data.messageSent = (o.Data.messageSent || 0) + 1;
	o.save();
};

//------------------------------------------------------------

}());